class Net_E(nn.Module):
    def __init__(self, encoder):
        super(Net_E, self).__init__()
        enc_layers = list(encoder.children())
        self.enc_1 = nn.Sequential(*enc_layers[:4])  # input -> relu1_1
        self.enc_2 = nn.Sequential(*enc_layers[4:11])  # relu1_1 -> relu2_1
        self.enc_3 = nn.Sequential(*enc_layers[11:18])  # relu2_1 -> relu3_1
        self.enc_4 = nn.Sequential(*enc_layers[18:31])  # relu3_1 -> relu4_1

        # fix the encoder
        for name in ['enc_1', 'enc_2', 'enc_3', 'enc_4']:
            for param in getattr(self, name).parameters():
                param.requires_grad = False

    # extract relu1_1, relu2_1, relu3_1, relu4_1 from input image
    def encode_with_intermediate(self, input):
        results = [input]
        for i in range(4):
            func = getattr(self, 'enc_{:d}'.format(i + 1))
            results.append(func(results[-1]))
        return results[1:]

    # extract relu4_1 from input image
    def encode(self, input):
        for i in range(4):
            input = getattr(self, 'enc_{:d}'.format(i + 1))(input)
        return input

    def forward(self, x):
        x_feat = self.encode(x)
        return x_feat


class Net_D(nn.Module):
    def __init__(self, encoder, decoder):
        super(Net_D, self).__init__()
        enc_layers = list(encoder.children())
        self.enc_1 = nn.Sequential(*enc_layers[:4])  # input -> relu1_1
        self.enc_2 = nn.Sequential(*enc_layers[4:11])  # relu1_1 -> relu2_1
        self.enc_3 = nn.Sequential(*enc_layers[11:18])  # relu2_1 -> relu3_1
        self.enc_4 = nn.Sequential(*enc_layers[18:31])  # relu3_1 -> relu4_1
        self.decoder = decoder

        # fix the encoder
        for name in ['enc_1', 'enc_2', 'enc_3', 'enc_4']:
            for param in getattr(self, name).parameters():
                param.requires_grad = False

    # extract relu1_1, relu2_1, relu3_1, relu4_1 from input image
    def encode_with_intermediate(self, input):
        results = [input]
        for i in range(4):
            func = getattr(self, 'enc_{:d}'.format(i + 1))
            results.append(func(results[-1]))
        return results[1:]

    # extract relu4_1 from input image
    def encode(self, input):
        for i in range(4):
            input = getattr(self, 'enc_{:d}'.format(i + 1))(input)
        return input

    def forward(self, x1, x2, x1_feat, x2_feat, rs, gamma, bbx1, bby1, bbx2, bby2):
        if torch.is_tensor(gamma):
            gamma = gamma.view(-1,1,1,1)
        g11 = x1
        g22 = x2
        g12 = gamma*x1 + (1.-gamma)*self.decoder(adain(x1_feat, x2_feat))
        g21 = gamma*x2 + (1.-gamma)*self.decoder(adain(x2_feat, x1_feat))

        Rc = torch.zeros(1, 1, 224, 224).cuda()
        Rc[:,:,bbx1:bbx2,bby1:bby2] = 1
        Rs = rs * torch.ones(1, 1, 224, 224).cuda()
        zero = torch.zeros(1, 1, 224 ,224).cuda()
        T = torch.max(zero,Rc+Rs-1)

        output = T*g11 + (1.0-Rc-Rs+T)*g22 + (Rc-T)*g12 + (Rs-T)*g21
        return output


